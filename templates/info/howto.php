<p>
Part of the UMBC Bohemian Week tradition is the preparation of certain foods,
for consumption during various Activity gatherings.
</p>
<p>
<span style="color: #0c0;">Tip:</span>
Since Bohemian Week is intended to take place during finals week, it's
important to remember that Activities must be balanced with work. Part of the beauty
