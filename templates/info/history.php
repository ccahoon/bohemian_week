<h3>The Spread of Bohemian Week</h3>
<div class="indent">
<p>
came the following spring, when those
Bohemian pioneers realized that they had been unforgivably selfish, and in
fact had violated the Bohemian Spirit, by not sharing their discovery.
To facilitate sharing of their new way of experiencing finals week, they
began calling it Bohemian Week, and they began calling their celebrations of
togetherness Activities. The first Bohemian Week flourished.
</p>
</div>
<h3>Activities</h3>
<div class="indent">
<p>
were so called for their active (as opposed to passive)
nature; those participants of the first Bohemian Week were encouraged, when
not working hard, to seek out their fellow Bohemians, simply to be together. The
founders realized that it was very easy to fall into the habit of being alone,
and sad, when faced with the pressure of finals, and the often lonely task of
preparing for them. It was therefore important to be active about enjoying
the company of friends at school during those final days of the semester.
</p>
</div>
