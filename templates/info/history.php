<p>
can be traced back to the winter of
&#39;10, when <a href="/info/founders">four room mates</a>, all students at the University of Maryland,
Baltimore County, decided that finals week was not a time of despair, but a
time to celebrate the togetherness of the past semester, as it came to a
close. They set their computers up in the common area of their on-campus
apartment, so that they could enjoy each other&#39;s company and support when
they were working hard. They worked hard so that they could finish their work
with time to spare for joyful celebrations of togetherness. They participated
in active celebrations of togetherness to remind themselves that life is good,
and of the immeasurable value of friendship.
</p>
